        <div class="paragraph wrong-region">
          <div class="h2-box">
            <h2>垃圾分类小误区</h2>
          </div>

          <h3>误区一：纸巾也是纸，可以回收利用</h3>
          <p>
            可回收的废纸主要包括报纸、期刊、图书、各种包装纸等（污染严重的纸除外）。在一般的认知中，纸巾属于纸张的一种，是可回收的。但实际上，厕纸遇水即溶，不算可回收的“纸张”。类似的还有餐巾纸、厨房用纸等，都属于“其他垃圾”。
          </p>
          <div class="img">
            <img src='../../assets/images/toilet-paper.jpg' alt="">
          </div>
          <h3>误区二：过期食品都是厨余垃圾</h3>
          <p>
            过期食品，比如一包受潮过期的瓜子，可以连带包装物一起丢进垃圾桶吗？这样做明显不妥！因为瓜子容易腐烂，而包装物（如塑料包装袋）装过这些过期的食品，已经被污染了，那么这些就都应该丢进“其他垃圾”桶。
          </p>
          <div class="img">
            <img src="../../assets/images/food.jpg" alt="">
          </div>
