        <div class="paragraph  main-problems">
          <div class="h2-box">
            <h2>主要问题</h2>
          </div>
          <h3>回收萎缩</h3>
          <p>
            国营垃圾回收日见萎缩。中国是最早提出垃圾分类收集的国家之一。就拿北京为例：1965年，二环路以内，国营废品收购站有2000多个。20世纪80年代后，其规模严重萎缩；1997年，三环路以内的国营废品收购站仅剩16个。
          </p>
          <h3>拾荒大军</h3>
          <p>
            拾荒大军取而代之问题多多。8.2万多的拾荒者，每年从北京“捡走”9.3亿元，同时还将废弃物转化成为新的资源，客观上减轻了政府处理垃圾的负担，减少18亿元的处理费用，延长了垃圾填埋处理场的使用寿命。但是，这些民间自发的拾荒者，均是无照经营，缺乏规范、检验和约束，致使垃圾在捡拾、收集、运输、加工过程中造成严重的二次污染，他们自身的传染病发病率也较高。
          </p>
          <h3>垃圾道</h3>
          <p>
            垃圾道成为百姓的苦恼。北京如今高楼林立，垃圾道成为许多地方的卫生死角，脏乱不堪的垃圾道口，蚊蝇十分张狂，加上拾荒者的胡乱翻找，使得居民们每当走过时必捂鼻而过，底层的住户可就无法逃避，在炎热的夏季也不敢打开门户。老百姓期待新型垃圾回收办法的实施。
          </p>

          <h3>袋装垃圾</h3>

          <p>
            袋装垃圾并非长久之计。北京一些地方开始实行封闭垃圾道，用塑料袋装垃圾定时回收的办法，以减少垃圾露天堆放对环境的影响。2000年2~5月期间，北京组织了“发展绿色流通，倡导绿色消费”的一次大型绿色消费调查。调查结果表明：消费者使用后的商场购物袋重复使用的占89.91%，其中，充当垃圾袋的占57.05%.
          </p>

          <h3>不懂垃圾分类</h3>

          <p>
            分类垃圾桶的困惑。北京一些街边虽然采用了分类垃圾桶，但许多人不知如何进行分类投放，不知道什么是可回收的垃圾。在一些社区，由于民间环保组织的倡导，也开展了垃圾分类的尝试，但环卫局的垃圾车又把分类好的垃圾倒到一起运走了，严重挫伤了百姓的环保积极性，产生了负面影响。可见，没有完整的收购、运输、销售、加工、成品市场等组织的再利用产业体系的支持，垃圾分类无法产生经济效益，只能是劳民伤财的空忙。
          </p>

          <h3>埋不胜埋，烧不胜烧</h3>

          <p>随着经济的发展和人民生活水平的提高，垃圾问题日益突出。中国668座城市，2/3被垃圾环带包围。这些垃圾埋不胜埋，烧不胜烧，造成了一系列严重危害。</p>
          <p>
            垃圾资源化潜力随着生活水平和经济的发展也不断增长。在垃圾成分中，金属、纸类、塑料、玻璃被视为可直接回收利用的资源，占垃圾总量的42.9%，可直接回收利用率应不低于33%。
          </p>
        </div>

        <div class="paragraph solution">
          <div class="h2-box">
            <h2>解决方法</h2>
          </div>

          <h3>一.依靠社区消费者</h3>
          <p>
            面对此现状，首先一定要尽快规范拾荒者的经营行为，加强引导和管理，在使其减少对社会不良影响的前提下，实现从无序到有序的经营企业的转变，充分发挥垃圾分类回收利用的作用。更重要的是，必须建立更加超前的消费者分类回收体系。
          </p>
          <h3>二.树立垃圾分类的观念</h3>
          <p>
            广泛开展垃圾分类的宣传、教育和倡导工作，使消费者树立垃圾分类的环保意识，阐明垃圾对社会生活造成的严重危害，宣传垃圾分类的重要意义，呼吁消费者积极参与垃圾分类。同时教会消费者垃圾分类的知识，使消费者进行垃圾分类逐渐成为自觉和习惯性行为。
          </p>

          <h3>三.改造或增设垃圾分类回收的设施</h3>
          <p>
            可将一个垃圾桶分割成几个隔段或建立几个独立的分类垃圾桶。垃圾分类应逐步细化。垃圾分类搞得越细越精，越有利于回收利用。可以用不同颜色的垃圾桶分别回收玻璃、纸、塑料和金属类包装垃圾、植物垃圾、生活垃圾、电池灯泡等特殊的垃圾。垃圾桶上必须注明回收的类别和简要使用说明，指导消费者使用。垃圾桶也可以成为企业广告的载体，企业可以承担制作费用。
          </p>
          <p>
            社区回收站可由社区物业或居委会负责管理，建立现代社区的垃圾经营和回收服务功能，使垃圾回收成为其创收的途径，贴补消费者卫生保洁费用的不足。政府可实行减免经营税的倾斜政策，来调动社区的管理积极性。新建小区更是要合理规划垃圾回收站，逐渐成为审批和验收的必备条件，强化新型社区的综合功能。
          </p>
          <h3>四.改善垃圾储运形式</h3>
          <p>
            对一些体积大的垃圾，应该压缩后进行储运。尤应注意的是，要对环卫局的垃圾回收车进行分隔式的改造，分类装载垃圾。充分发挥原有垃圾回收渠道的作用，将可再生利用的垃圾转卖到企业。另外，建立垃圾下游产业的专门回收队伍，由厂家直接回收，实现多渠道回收，引入价格和服务的竞争机制，以此提高他们的服务质量和垃圾的回收率。
          </p>
          <h3>五.实行家庭短期收集，定期分时段分类回收</h3>

          <p>
            可以将垃圾分类一星期内暂时由家庭保管，环卫每天早晨收集容易腐烂的菜叶等餐厨垃圾，每天中午收集可以回收利用的垃圾和建筑垃圾等，晚上收集其他垃圾。社区或物业管理部门定期对新来户上门指导或发宣传册，让居民都知道如何垃圾分类。有条件的地方，还要定期播放学习国外环保收集垃圾的视频，提高本地区居民的素质，养成爱护环境就是爱护自己的习惯。
          </p>
